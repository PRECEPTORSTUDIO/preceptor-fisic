	<li>Conteúdo de diretrizes (ACSM, AHA, etc) usado em RAG é citado em cada recomendação e pertence aos respectivos titulares</li>
	<li>Dados clínicos de alunos pertencem ao próprio aluno; profissionais são <strong>controladores conjuntos</strong></li>
	<li>Você mantém propriedade sobre planos que cria — concedemos licença não exclusiva para hospedagem</li>
</ul>

<h2>7. Suspensão e encerramento</h2>
<p>
	Podemos suspender contas que violem estes Termos. Você pode encerrar sua conta a qualquer
	momento via Configurações. Em caso de encerramento:
</p>
<ul>
	<li>Dados pessoais são deletados em até 30 dias</li>
	<li>Auditoria de prescrições é mantida por 5 anos (obrigação clínico-legal)</li>
	<li>Alunos vinculados recebem aviso e podem solicitar exportação dos próprios dados</li>
</ul>

