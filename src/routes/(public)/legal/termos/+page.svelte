<h1>Termos de Uso</h1>
<div class="legal-meta">Última atualização: 7 de maio de 2026</div>

<p>
	Bem-vindo ao Preceptor Fisic. Ao acessar ou usar nossos serviços, você concorda com estes
	Termos. Leia com atenção — eles definem direitos e responsabilidades de profissionais
	(CREF/CREFITO/CRM) e dos alunos vinculados.
</p>

<h2>1. Objeto e escopo</h2>
<p>
	O Preceptor Fisic é uma plataforma de software como serviço (SaaS) destinada a
	<strong>profissionais habilitados em Educação Física, Fisioterapia e Medicina</strong> que
	prescrevem exercícios para populações especiais. A plataforma oferece:
</p>
<ul>
	<li>Cadastro e gerenciamento de alunos com histórico clínico</li>
	<li>Geração assistida por IA de planos de treino fundamentados em diretrizes técnicas (ACSM, ESSA, AHA, OMS)</li>
	<li>Validação automática contra regras clínicas pré-cadastradas</li>
	<li>Aplicativo mobile para o aluno acompanhar treinos prescritos</li>
</ul>
<p>
	A plataforma <strong>não substitui</strong> diagnóstico médico, avaliação clínica
	presencial ou julgamento profissional. Toda prescrição final é de responsabilidade
	exclusiva do profissional inscrito no respectivo conselho.
</p>

<h2>2. Cadastro e conta</h2>
<h3>2.1. Profissional</h3>
<p>
	Para criar conta como profissional, você declara possuir habilitação ativa em CREF, CREFITO,
	CRM ou registro equivalente. Reservamo-nos o direito de solicitar comprovação a qualquer
	momento e suspender contas que descumpram este requisito.
</p>
<h3>2.2. Aluno</h3>
<p>
	Alunos acessam a plataforma exclusivamente via convite (link mágico) enviado pelo
	profissional cadastrado. <strong>Não há cadastro autônomo de alunos</strong>. O aluno deve
	ter consentido com o tratamento dos seus dados para finalidade clínica antes de o
	profissional cadastrá-lo.
</p>

<h2>3. Uso aceitável</h2>
<p>Você concorda em não:</p>
<ul>
	<li>Compartilhar credenciais de acesso ou cadastrar alunos sem consentimento</li>
	<li>Inserir dados sensíveis de terceiros sem base legal (Lei 13.709/2018, art. 11)</li>
	<li>Usar a IA para fins fora da prescrição clínica de exercício físico</li>
	<li>Tentar burlar limites de uso (rate limit, quotas) ou engenharia reversa do sistema</li>
	<li>
		Publicar planos sem revisão profissional. <strong>Toda prescrição publicada implica
		validação clínica do profissional.</strong>
	</li>
</ul>

<h2>4. Geração por IA e responsabilidade clínica</h2>
<p>
	Os planos gerados pela IA usam contexto de diretrizes (ACSM com prioridade) e são validados
	contra regras clínicas automatizadas. Ainda assim:
</p>
<ul>
	<li>O conteúdo gerado é uma <strong>recomendação técnica</strong>, não diagnóstico</li>
	<li>O profissional deve revisar e adaptar antes de publicar</li>
	<li>
		Restrições "vermelhas" (CRÍTICO) bloqueiam publicação automaticamente, mas a ausência de
		restrição não substitui revisão clínica
	</li>
	<li>Auditamos cada geração via tabela <code>ai_runs</code> para rastreabilidade</li>
</ul>

<h2>5. Limitações de uso e cobrança</h2>
<p>
	Atualmente o serviço opera em <strong>fase beta gratuita</strong>. Aplicamos limites
	técnicos para evitar abuso (ex: 5 planos a cada 5 minutos por profissional). Cobranças
	futuras serão comunicadas com no mínimo 30 dias de antecedência.
</p>

